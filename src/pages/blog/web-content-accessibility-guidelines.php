        <div class="large-12 columns">
          <p class="fadeInDown wow" ><small><u><a href="http://www.corey-noble.com/blog.php">Back to Blog</a></u></small></p>
          <h3 class="fadeInDown wow" ><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/Overview.html" title="WCAG Guidelines" target="_blank"><b><u>Understanding WCAG 2.0</u></b></a></h3>
          <p class="fadeInDown wow" >The WCAG guidelines are a legal requirement that all websites of companies that employ more than 50 people are required to meet. There are two ways to approach issues related to accessible websites and their content:</p>
          <p class="fadeInDown wow" >1. <b>Focus on people</b>.<br>
          Concider the needs of the people on your website with disabilities.</p>
          <p class="fadeInDown wow" >2. <b>Focus on technology</b>.<br>
          Meet the technical requirements that assist the people on your website with disabilities.</p>
            
          <hr class="fadeInDown wow">
            
          <p class="fadeInDown wow" >The World Wide Web Consortium (W3C) – maintains a widely accepted set of technical guidelines for accessible websites.</p>
          <p class="fadeInDown wow" >The Web Content Accessibility Guidelines (WCAG 2.0) contains a comprehensive set of documents, specifications and techniques dealing with all aspects of accessibility for websites and web content.</p>
          <p class="fadeInDown wow" >WCAG 2.0 is structured around 4 major questions:</p>
          <p class="fadeInDown wow" >1.	Is it perceivable?<br>
          2.	Is it operable?<br>
          3.	Is it understandable?<br>
          4.	Is it robust?</p>
            
          <hr class="fadeInDown wow">

          <p class="fadeInDown wow" ><b>Perceivable</b> – information and user interface components must be presentable to users in ways they can perceive.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/text-equiv.html" title="Text Alternatives" target="_blank">1.1 Text Alternatives</u> – Provide text alternatives for any non-text content so that it can be changed into other forms people need, such as large print, braille, speech, symbols or simpler language.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/media-equiv.html" title="Time-based Media" target="_blank">1.2 Time-based Media</u> - Provide alternatives for time-based media.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/content-structure-separation.html" title="Adaptable" target="_blank">1.3 Adaptable</u> – Create content that can be presented in different ways (for example simpler layout) without losing information or structure.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/visual-audio-contrast.html" title="Distinguishable" target="_blank">1.4 Distinguishable</u> – Make it easier for users to see and hear content including separating foreground from background.</p>
              
          <hr class="fadeInDown wow">
              
          <p class="fadeInDown wow" ><b>Operable</b> – User interface components and navigation must be operable.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/keyboard-operation.html" title="Keyboard accessible" target="_blank">2.1 Keyboard accessible</u> – Make all functionality available from a keyboard.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/time-limits.html" title="Enough Time Media" target="_blank">2.2 Enough Time Media</u> – Provide users enough time to read and use content.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/seizure.html" title="Seizures" target="_blank">2.3 Seizures</u> – Do not design content in a way that is known to cause seizures.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/navigation-mechanisms.html" title="Navigable" target="_blank">2.4 Navigable</u> – Provide ways to help users navigate, find content, and determine where they are.</p>
              
          <hr class="fadeInDown wow">
              
          <p class="fadeInDown wow" ><b>Understandable</b> – information and the operation of user interface must be understandable.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/meaning.html" title="Readable" target="_blank">3.1 Readable</u> – Make text content readable and understandable.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/consistent-behavior.html" title="Predictable" target="_blank">3.2 Predictable</u> – Make Web pages appear and operate in predictable ways.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/minimize-error.html" title="Input Assistance" target="_blank">3.3 Input Assistance</u> – Help users avoid and correct mistakes.</p>
              
          <hr class="fadeInDown wow">
              
          <p class="fadeInDown wow" ><b>Robust</b> – content must be robust enough that is can be interpreted reliably by a wide variety of user agents, including assistive technologies.</p>
          <p class="fadeInDown wow" ><u><a href="https://www.w3.org/TR/UNDERSTANDING-WCAG20/ensure-compat.html" title="Compatible" target="_blank">4.1 Compatible</u> - Maximize compatibility with current and future user agents, including assistive technologies.</p>

          
          <hr class="fadeInDown wow">
              
          <p class="fadeInDown wow" ><a href="http://www.corey-noble.com/assets/pdf/WCAG2Checklist.pdf" title="WCAG 2.0 Checklist" target="_blank"><b><u>Requirement Checklist (.pdf checklist)</u></b></a></p>
          <p class="fadeInDown wow" >Level A (required by law) – the most basic web accessibility features.<br>
          Level AA (required by law) – deals with the biggest and most common barriers for disabled users<br>
          Level AAA (recommended but not required by law) – the highest (and most complex) level of web accessibility<br>

