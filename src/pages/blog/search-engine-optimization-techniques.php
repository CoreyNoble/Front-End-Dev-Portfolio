          <h3 class="fadeInDown wow" >How do Search Engines&nbsp;Operate?</h3>
          <p class="fadeInDown wow" >Popular search engines like <a href="http://www.google.com/" title="Google" target="_blank"><u>Google</u></a> and <a href="http://www.bing.com/" title="Bing" target="_blank"><u>Bing</u></a> rank websites by comparing the relevance of the content on a website, with the context of a users query. The more relevant and your website is to the query determines how high up on the list a search engine will display your&nbsp;website.</p>
          <p class="fadeInDown wow" >Search engines have two major&nbsp;functions:</p>
          <ol>
            <li class="fadeInDown wow" >Crawling everything on the web, incluing: websites, pages, files and documents. Then building a ranked index of the web content, based on topic relevance, quality and accessibility of the&nbsp;content.</li>
