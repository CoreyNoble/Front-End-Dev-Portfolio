          <h3 class="fadeInDown wow" >What is Search Engine Ranking and Why is it&nbsp;Important?</h3>
          <p class="fadeInDown wow" >The rank of a webpage determines how high up on the search list the page is displayed. Page rank is based on a number of different&nbsp;factors.</p>
          <p class="fadeInDown wow" >One of the most important factors is the quantity, quality and popularity of external websites that contain links pointing to your webpage. The more external websites that point to your website, the easier it is for search engines to validate the importance of your website. This also attracts more users to your website which in-turn shows the search engine that your content&nbsp;matters. If your brand or website does not have a lot of recognition, this may be diserning. Not to worry, there are many other ways to increase page rank.</p>
          <p class="fadeInDown wow" >It is important to reasearch your toughest competition. If you know how they cater their content towards the public, you can adjust yours to have more contextual meaning and relevance than theirs. Just as importantly, you can better cater to the demographic of person viewing your content, moreso than your competition&nbsp;does.</p>
