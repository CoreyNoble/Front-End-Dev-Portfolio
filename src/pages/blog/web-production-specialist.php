          <!-- Agile Methodoligy -->
          <h3 class="fadeInDown wow">Agile Methodoligy</h3>
          <p class="fadeInDown wow">Thrillworks <b>used</b> to take a waterfall approach to project work. A waterfall process involves starting a project with one department (UX) and moving through the other departments (Design/Copy > Development/Production > QA) one after another, until a project is complete. This was problematic as the departments further down the chain would often see their timelines cut due to discrepencies with departments up the chain. This created stress and affected profit margins.</p>
          <p class="fadeInDown wow">Their solution to this problem was to completely revamp the way work was done at Thrillworks. The company brought in specialists on Agile development and the everyone learned Agile Methodoligy.</p>
          <p class="fadeInDown wow">Agile Methodoligy is a very different approach to project work, opposed to the typical waterfall approach. It allows Thrillworks to increase profit and accellerate timelines.</p>
          <p class="fadeInDown wow">A breif synopsis of Agile:</p>
          <ul>
            <li class="fadeInDown wow">No more departments. Teams are created to handle specific clients. These teams include the skillsets that cover the needs of the client.</li>
            <li class="fadeInDown wow">A new workflow process in-which a project is broken down into pieces of work, as granularly as possible and marked with time estimates. All of the pieces of work are put up on a board and categoriezed by their stauts: backlog, in progress, blocked, ready for review or completed. Tasks are taken by a team member with the skills to work on it, they 'own' it for the time they are working on it. Due to this approach, some work that would normally start later in the process can start almost immediately. Nobody is ever working on the same work at the same time, this avoids project collision.</li>
            <li class="fadeInDown wow">Daily team meetings occur to bring up concerns and keep the status of projects in-check.</li>
            <li class="fadeInDown wow">Retrospectives meetings are scheduled to help iron out issues that came up throughout a project. This helps improve upon processes for the next time.</li>
            <li class="fadeInDown wow">Employees are T-shaped, meaning that they are encouraged to help out when they can, even if it is a different workflow or technology that they are not used to. This helps ensure there are no knowledge silos and that teams share the workload.</li>
          </ul>
            
