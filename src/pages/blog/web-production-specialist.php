          <h3 class="fadeInDown wow">What is Web Production Specialist?</h3>
          <p class="fadeInDown wow">In short, a Web Production Specialist at Thrillworks is a 'jack of all trades' role with focus on content authoring, email campaigns, web production graphics, and front-end web development.</p>
          <!-- Finish Intro -->
          <p class="fadeInDown wow">Below are examples of various tasks that may be required of my position, on a day-to-day basis.</p>
          
          <hr class="fadeInDown wow">
          
          <!-- Email Campaigns -->
          <h3 class="fadeInDown wow">Email Campaigns</h3>
          <p class="fadeInDown wow">Using technologies like Exact Target and Customer.io, I build, test and deploy .html and .txt email campaigns.</p>
          <p class="fadeInDown wow">Typical Process:</p>
          <ol>
            <li class="fadeInDown wow">I am provided .psd designs of approved emails. I slice out the images that I need and save them out as web graphics with specific naming conventions.</li>
            <li class="fadeInDown wow">I upload all of the sliced images to the email client server and note their final URLs and dimensions so I can rapidly add images to the .html later on.</li>
            <li class="fadeInDown wow">I copy a pre-existing email with the structure that is closest to what I need. Typically the header and footer stay the same as a pre-existing email so copying is more expedient to the process.</li>
            <li class="fadeInDown wow">I gut the copied emails (.html and .txt) and keep only the structure that will be required for the build.</li>
            <li class="fadeInDown wow">I build out the emails by referencing any spreadsheets, copydecks and designs that have been provided. This is when I add all of the images, anylitics, alt text, links, and copy.</li>
            <li class="fadeInDown wow">When done, I send test emails to internal staff for review.</li>
            <li class="fadeInDown wow">After the review process, typically a few items will need to change. I make any nessesary updates and re-send test emails.</li>
            <li class="fadeInDown wow">When all emails are ready to be sent there can be some additional criteria to be met. That can involve:
                <ul>
                    <li>Setting up a scheduled deployment in batches (eg. 15,000/hr) to account for server load on popular campaigns.</li>
                    <li>Partitoning subscibers for region specific campaigns.</li>
                    <li>Hooking up dynamically generated content (eg. QR Codes).</li>
                </ul>
            </li>
          </ol>
            
