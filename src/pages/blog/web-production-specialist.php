          <!-- Agile Methodoligy -->
          <h3 class="fadeInDown wow">Agile Methodoligy</h3>
          <p class="fadeInDown wow">Thrillworks used to have a waterfall approach to web development. This means that a project would start with one department, and make its way through the departments until a project was completed. This was problematic as projects could get stuck and timelines would be tight as a result, creating stress and affecting the bottom line. Their solution to this problem was to completely revamp the way work was done at Thrillworks. They brought in Agile specialists and the entire company learned Agile Methodoligy.</p>
          <p class="fadeInDown wow">Agile Methodoligy includes:</p>
          <ul>
            <li class="fadeInDown wow">No more departments. Teams are created to handle specific clients and include the skillsets that cover the needs of the client.</li>
            <li class="fadeInDown wow">A new workflow process in-which a project is broken down into pieces of work, as granularly as possible. All of the pieces of work are put up on a board in 'swim-lanes' and marked as either backlog, in-progress, blocked, staging, completed. Due to this approach, work can start almost immediately, with enough oversight for everyone to be able to work individually without affecting another.</li>
            <li class="fadeInDown wow">Daily team meetings occur to bring up concerns and keep the status of projects in-check.</li>
            <li class="fadeInDown wow">Retrospectives meetings are scheduled to help iron out issues that came up throughout a project. This helps improve upon processes for the next time.</li>
            <li class="fadeInDown wow">Employees are T-shaped, meaning that they are encouraged to help out when they can, even if it is a different workflow or technology that they are not used to. This helps ensure there are no knowledge silos and teams work well together.</li>
          </ul>
            
